    In-App결제가 법정대리인으로부터 처분을 허락받은 재산의 범위 내인 경우 또는
    미성년자가 사술 등을 사용하여 성년자로 믿게 한 때에는 취소가 제한됩니다.
    유료콘텐츠 구매자가 미성년자인지 여부는 In-App결제가 진행된 단말기 또는
    신용카드 등 결제수단의 명의자를 기준으로 판단됩니다. 미성년자의 결제취소를
    요청하시는 경우 회사의 필요에 따라 미성년자 및 법정대리인을 증명할 수 있는
    서류를 제출하여야 합니다.
    <br />
    <br />
    8. In-App결제는 앱스토어사업자가 제공하는 결제방식에 따르며. In-App결제
    과정에서 과오금이 발생하는 경우 앱스토어사업자에게 환불을 요청하여야 합니다.
    만약, In-App 결제과정에서 과오금이 발생하여 회사에 환불(결제취소 포함) 등을
    요청하는 경우 회사는 앱스토어사업자의 정책, 시스템에 따라 가능한 경우에
    한하여 앱스토어사업자에게 과오금 환불을 요청하여 환불을 진행하며, 과오금
    환불 방식은 제14조 제3항에 따릅니다. 다만, 앱스토어사업자가 과오금 환불을
    제한하고 있는 경우에는 회원이 직접 앱스토어사업자에게 과오금 환불을
    요청하여야 합니다.
