    - 구매 후 즉시 사용이 시작되거나 즉시 서비스에 적용되는 유료콘텐츠의 경우
    <br />
    - 추가적인 혜택이 제공된 유료콘텐츠 구매 후 해당 추가 혜택이 사용된 경우
    <br />
    - 묶음형(패키지형)으로 판매된 유료콘텐츠의 일부가 사용된 경우
    <br />
    - 개봉행위를 사용으로 볼 수 있거나 개봉 시 효용이 결정되는 캡슐형/확률형
    유료콘텐츠를 개봉한 경우
    <br />
    - 유료콘텐츠의 일부를 사용하였거나 이미 시간이 지나 다시 판매하기 곤란한
    경우
