    제공하는 비밀번호 설정기능 등을 이용하여 제3자의 In-App결제를 방지하여야
    합니다.
    <br />
    <br />
    3. 회사는 회원이 In-App결제 방지기능 등을 이용하지 않거나 회원의 부주의로
    비밀번호가 노출되어 발생하는 제3자의 In-App결제에 대해 어떠한 책임도
