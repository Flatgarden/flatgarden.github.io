    환불 정책에 따라 진행되며, 자세한 환불 신청절차는 앱스토어사업자의
    운영정책에 따릅니다. 또한 구매한 유료콘텐츠에 대한 환불이 완료된 경우에는
    환불 금액만큼 유료콘텐츠가 차감됩니다. 또한, 유료콘텐츠가 다르게 지급된
    경우에는 본조 제2항에서 정한 방법으로 환불을 합니다. 다만, 회원이 다르게
    지급된 유료콘텐츠의 일부를 사용한 경우에는 동일·유사한 유료콘텐츠로만
    보상합니다.
    <br />
    <br />
    4. 유료콘텐츠를 통해 무료콘텐츠로 전환한 경우, 전부 또는 일부 사용으로
    인하여 서비스내에서 구매할 수 있는 기본 단위가 훼손된 경우, 메시지함 및/또는
    선물함 등 유료콘텐츠의 송/수신과 관련된 화면에서 수락 등의 동의 과정을 거친
    경우 등 회원의 유료콘텐츠 이용으로 간주될 수는 사유가 있는 때에는 회원이
    사용의사를 밝힌 것으로 간주되며, 회원의 의도와 상이하게 타인을 통해
    구매되었을 경우도 포함합니다.
