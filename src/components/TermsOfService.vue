    인하여 결제금액에 차이가 발생할 수 있습니다. 또한 유료콘텐츠의 구매 대금은
    앱스토어사업자와 연계된 이동통신사나 플랫폼사업자 및/또는 앱스토어사업자가
    정하는 방법 및 정책에 따라 부과되며, 납부방식 역시 해당 사업자의 결제정책을
    따릅니다.
    <br />
    <br />
    2. 회원이 서비스에서 구매한 유료콘텐츠는 해당 서비스 애플리케이션을 다운로드
    받아 설치한 단말기에서만 이용할 수 있습니다.
    <br />
    <br />
    3. 회원이 구매한 유료콘텐츠는 구매 후 특정한 기한의 제약을 받지 않고 소비할
    수 있습니다. 단, 사용기간이 별도로 명시된 유료콘텐츠의 경우에는 구매시
    명시된 사용기간에 따릅니다. 회원은 회사가 정하여 별도로 고지한 방법 이외에는
    유료콘텐츠를 회원 계정에서만 이용할 수 있으며, 제3자에게 양도, 대여, 매매
    또는 환전 등을 할 수 없습니다.
