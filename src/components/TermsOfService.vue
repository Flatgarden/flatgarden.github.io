    「전자상거래 등에서의 소비자보호에 관한 법률」 및 같은 법 시행령에서 정하는
    이율을 곱하여 산정한 지연이자를 지급합니다.
    <br />
    <br />
    3. 회사는 환불을 함에 있어 회원이 신용카드, 그 밖에 「전자상거래 등에서의
    소비자보호에 관한 법률 시행령」에서 정한 결제수단으로 대금을 지급한 때에는
    지체없이 당해 결제수단을 제공한 사업자로 하여금 대금의 청구를 정지 또는
    취소하도록 요청합니다. 다만, 회사가 결제업자로부터 이미 대금을 지급받은
    때에는 이를 결제업자에게 환불하고 이를 소비자에게 통지합니다.
    <br />
    <br />
    4. 회원이 제13조 제1항 본문에 따른 청약철회를 한 경우 유료콘텐츠 등의 반환에
    필요한 비용은 회원이 부담하고, 회사는 회원에게 청약철회를 이유로 위약금 또는
    손해배상을 청구하지 않습니다.
