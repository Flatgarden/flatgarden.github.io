    이용행위로 취득하거나 다른 회원으로부터 선물받은 유료콘텐츠, 회사의 이벤트
    등을 통하여 취득한 무료콘텐츠는 환불되지 않습니다.
    <br />
    <br />
    6. 회사는 청약철회가 제한되는 유료콘텐츠에 대해 In-App결제 전에 청약철회가
    제한됨을 표시하는 등의 방법으로 청약철회 등의 권리행사가 방해받지 아니하도록
    조치합니다. 만약 회사가 이러한 조치를 하지 아니한 경우에는 청약철회 제한
    사유에도 불구하고 회원은 청약철회를 할 수 있습니다. 다만, 다음 각호의
    경우에는 청약철회가 제한됩니다.
