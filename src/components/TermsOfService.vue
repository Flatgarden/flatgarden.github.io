    요청 시 이미 사용하였거나 사용한 것으로 간주되는 유료콘텐츠 등 이에 준하는
    특성을 가진 유료콘텐츠에 대하여는 「전자상거래 등에서의 소비자보호에 관한
    법률」 제17조 제2항에 따라 청약철회(구매취소)가 제한될 수 있습니다.
    <br />
    <br />
    2. 회사는 회사의 귀책사유로 인하여 회원이 구매한 유료콘텐츠를 서비스에서
    이용하지 못하는 경우, 동일·유사한 유료콘텐츠로 보상하거나 해당 구매 금액을
    구매일에 관계없이 전액 환불합니다.
